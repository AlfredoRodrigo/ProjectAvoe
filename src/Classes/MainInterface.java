    private void defColor(JPanel btn1, JPanel btn2, JPanel btn3, JPanel btn4){
        this.setColor(btn1);
        this.resetColor(btn2);
        this.resetColor(btn3);
        this.resetColor(btn4);
    }
    
    private void defPanel(JPanel panel1, JPanel panel2, JPanel panel3, JPanel panel4){
        panel1.setVisible(true);
        panel2.setVisible(false);
        panel3.setVisible(false);
        panel4.setVisible(false);
    }

